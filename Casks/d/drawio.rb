cask "drawio" do
  arch arm: "arm64", intel: "x64"

  version "29.5.2"
  sha256 arm:   "f5117ad0b9cc4ef10b5b434de5f1c57de9f7a7855c827d3df6e07edb69111fea",
         intel: "695b5654962b7b8bf7d8964f3245c15959acf23846aee0c4fb1b9b2a0a032d9a"

  url "https://mirror.ghproxy.com/https://github.com/jgraph/drawio-desktop/releases/download/v#{version}/draw.io-#{arch}-#{version}.dmg",
      verified: "github.com/jgraph/drawio-desktop/"
  name "draw.io Desktop"
  desc "Online diagram software"
  homepage "https://www.diagrams.net/"

  livecheck do
    url :url
    strategy :github_latest
  end

  auto_updates true
