  arch arm: "-arm64"

  version "1.102.0-insider,1751904751524,235960dfc9a9f3215a06031a730ca68af0c4a68d"
  sha256 arm:   "223a4c64bee423ae6996a48f5c4ce1108417058c092dd9b90b6b12b020ed77c1",
