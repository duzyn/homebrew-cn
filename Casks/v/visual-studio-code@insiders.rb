  arch arm: "-arm64"

  version "1.110.0-insider,1771866410210,62028559c1ca63edaa7fd32d3845ea502af07ab2"
  sha256 arm:   "71390dc992fc140e94fd8886ef76ad0452c3260bc49962fe76d7715e222dddf2",
