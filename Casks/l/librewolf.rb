cask "librewolf" do
  arch arm: "arm64", intel: "x86_64"

  on_arm do
    version "124.0.1,1"
    sha256 "6275cbdab1ec0594b808237cbc3082e9c6914132119d7da51ef9519c6d7fedd4"
  end
  on_intel do
    version "124.0.1,1"
    sha256 "d168c0108b29b437f94cd7213e6c5ad28e602e29c5d28f50ffa4c3d7b701eb34"
  end

  url "https://gitlab.com/api/v4/projects/44042130/packages/generic/librewolf/#{version.csv.first}-#{version.csv.second}/librewolf-#{version.csv.first}-#{version.csv.second}-macos-#{arch}-package.dmg",
