class Victorialogs < Formula
  desc "Open source user-friendly database for logs from VictoriaMetrics"
  homepage "https://docs.victoriametrics.com/victorialogs/"
  url "https://mirror.ghproxy.com/https://github.com/VictoriaMetrics/VictoriaLogs/archive/refs/tags/v1.46.0.tar.gz"
  sha256 "ce07a5c2d8fb742a0fcc05a712f73c4b693269f35392443dbc78cf75692e397b"
  license "Apache-2.0"

  # The Git tags are interspersed with higher versions like 1.118.0, so we check
  # the "latest" release instead of the Git tags.
  livecheck do
    url :stable
    regex(/^v?(\d+(?:\.\d+)+)$/i)
    strategy :github_latest
  end

  bottle do
    sha256 cellar: :any_skip_relocation, arm64_tahoe:   "a964c5edd0c6d0b6410c977b0e6f2f3a0cb9275b8556e2060566eb0654829b8b"
    sha256 cellar: :any_skip_relocation, arm64_sequoia: "908c267a86f3f9747ffa0d66d63ebff729d16b827cf8f78f176340e94edcecab"
    sha256 cellar: :any_skip_relocation, arm64_sonoma:  "6fb0828e221e545e3a4a06cbb6d48253ecaf4fd50723139fb211feaefb44f9d6"
    sha256 cellar: :any_skip_relocation, sonoma:        "55849dbe446f85e45c8904489d074802d039d176af6b4e94fb50bf54499a90f7"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "8c55224fcf4d6caaac7a6df0fc71cdb03f9cae0dc72a1fd3bc3174aeefbb875d"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "7344aed98c368dba55db900796c55bb65225398b4961b627611a0b0c0061545c"
  end

  depends_on "go" => :build

  def install
    ldflags = "-s -w -X github.com/VictoriaMetrics/VictoriaMetrics/lib/buildinfo.Version=#{version}"
    system "go", "build", *std_go_args(ldflags:, output: bin/"victoria-logs"), "./app/victoria-logs"
  end

  service do
    run [
      opt_bin/"victoria-logs",
      "-httpListenAddr=127.0.0.1:9428",
      "-storageDataPath=#{var}/victorialogs-data",
    ]
    keep_alive false
    log_path var/"log/victoria-logs.log"
    error_log_path var/"log/victoria-logs.err.log"
  end

  test do
    http_port = free_port

    pid = spawn bin/"victoria-logs",
                "-httpListenAddr=127.0.0.1:#{http_port}",
                "-storageDataPath=#{testpath}/victorialogs-data"
    sleep 5
    assert_match "Single-node VictoriaLogs", shell_output("curl -s 127.0.0.1:#{http_port}")

    assert_match version.to_s, shell_output("#{bin}/victoria-logs --version")
  ensure
    Process.kill("TERM", pid)
    Process.wait(pid)
  end
end
